// Condicionales con booleanos
let booleano = false;
if (booleano == true) {
    console.log("Verdadero");
}
else {
    console.log("Falso");
}
// Condicionales con igualdad de valores de una variable
let numero = 32;
if (numero == 32) {
    console.log("Es 32");
}
else {
    console.log("No es 32");
}
// Condicionales con mas de 2 respuestas a la condicion
let numero2 = 5;
if (numero2 == 5) {
    console.log("Es 5");
}
else if (numero2 > 5){
    console.log("Es mayor a 5");
}
else {
    console.log("Es menor a 5");
}
// Bucle booleano
let ciclo = true;
while (ciclo == true) {
    console.log("Ciclo");
    ciclo = false;
}
// Bucle iterando por un contador hasta el numero 10
for (let i = 0; i <= 10; i++) {
    console.log("bucle n" + i); // Por cada iteracion se hace un output bucle n i
}

